# -*- coding: utf-8 -*-
"""基准测试结果自动生成的 Markdown 报告。

生成一份全面的 report.md，包含：
- 实验配置摘要
- 每个算法的统计信息
- 指标对比表
- 关键发现
"""

import logging
from datetime import datetime
from pathlib import Path
from typing import Dict

from common.dataclasses import AlgorithmResult
from utils.file_io import ensure_dir

logger = logging.getLogger(__name__)


def generate_report(
    algo_results: Dict[str, AlgorithmResult],
    metric_summary: Dict[str, Dict[str, Dict[str, float]]],
    config_dict: dict,
    output_dir: Path,
) -> None:
    """生成 Markdown 实验报告。

    参数：
        algo_results: 算法结果。
        metric_summary: 来自 BatchEvaluator.evaluate()。
        config_dict: 配置参数字典。
        output_dir: 报告的输出目录。
    """
    ensure_dir(output_dir)
    report_path = output_dir / "report.md"

    lines = _build_report_lines(algo_results, metric_summary, config_dict)

    with open(report_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    logger.info(f"Report written to {report_path}")


def _build_report_lines(
    algo_results: Dict[str, AlgorithmResult],
    metric_summary: Dict[str, Dict[str, Dict[str, float]]],
    config_dict: dict,
) -> list:
    """将报告内容构建为行列表。

    参数：
        algo_results: 算法结果。
        metric_summary: 指标摘要。
        config_dict: 配置字典。

    返回：
        报告行列表。
    """
    lines = []
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    # ---- 标题 ----
    lines.append("# WSI Patch Selection Benchmark Report")
    lines.append("")
    lines.append(f"**Generated**: {now}")
    lines.append("")

    # ---- 配置 ----
    lines.append("## 1. Experiment Configuration")
    lines.append("")
    lines.append("| Parameter | Value |")
    lines.append("|-----------|-------|")
    key_params = [
        "patch_size", "patches_per_case", "seed", "stride",
        "innovation_weight", "min_center_distance_ratio",
    ]
    for key in key_params:
        if key in config_dict:
            lines.append(f"| {key} | {config_dict[key]} |")
    lines.append("")

    # ---- 算法摘要 ----
    lines.append("## 2. Algorithm Summary")
    lines.append("")
    lines.append(
        "| Algorithm | Cases | OK | Partial | Failed | Patches Saved |"
    )
    lines.append(
        "|-----------|-------|-----|---------|--------|---------------|"
    )

    algo_display = {
        "random": "Random",
        "grid": "Grid",
        "largest_tissue": "Largest Tissue",
        "stratified": "Stratified",
        "kmeans": "K-Means",
        "yottixel": "Yottixel",
        "splice": "SPLICE",
        "sdm": "SDM",
        "sentinel": "Sentinel (SAPS)",
    }

    for algo_name in sorted(algo_results.keys()):
        r = algo_results[algo_name]
        display = algo_display.get(algo_name, algo_name)
        lines.append(
            f"| {display} | {r.total_cases} | {r.total_ok} | "
            f"{r.total_partial} | {r.total_failed} | "
            f"{r.total_patches_saved} |"
        )
    lines.append("")

    # ---- 指标对比 ----
    lines.append("## 3. Representative Metrics")
    lines.append("")

    # 构建表头
    metric_keys = []
    for algo_name in metric_summary:
        for key in metric_summary[algo_name]:
            if not key.startswith("_"):
                metric_keys.append(key)
        break  # 只需要一个算法的键即可

    header = "| Algorithm |"
    for mk in metric_keys:
        header += f" {mk} (mean±std) |"
    lines.append(header)
    lines.append("|" + "---|" * (len(metric_keys) + 1))

    for algo_name in sorted(algo_results.keys()):
        display = algo_display.get(algo_name, algo_name)
        row = f"| {display} |"
        if algo_name in metric_summary:
            for mk in metric_keys:
                stats = metric_summary[algo_name].get(mk, {})
                mean_val = stats.get("mean", 0.0)
                std_val = stats.get("std", 0.0)
                row += f" {mean_val:.4f}±{std_val:.4f} |"
        else:
            row += " N/A |" * len(metric_keys)
        lines.append(row)
    lines.append("")

    # ---- 关键发现 ----
    lines.append("## 4. Key Findings")
    lines.append("")

    # 找出每个指标的最佳算法
    for mk in metric_keys:
        best_algo = None
        best_val = -float("inf")
        for algo_name, stats in metric_summary.items():
            val = stats.get(mk, {}).get("mean", 0.0)
            if val > best_val:
                best_val = val
                best_algo = algo_name
        if best_algo:
            display = algo_display.get(best_algo, best_algo)
            lines.append(f"- **Best {mk}**: {display} ({best_val:.4f})")

    lines.append("")
    lines.append("---")
    lines.append("*Report auto-generated by WSI Patch Selection Benchmark*")

    return lines
